    public void AutoID()
    {
        try
        {
            ste=con.createStatement();
            rs=ste.executeQuery("Select * From Employee Order By EmployeeID Desc");
            int id=0;
            if(rs.next())
            {
                id=rs.getInt(1);
            }
            if(id==0)
            {
                lblEmployeeID.setText("1");
            }
            else
            {
                lblEmployeeID.setText(""+(id+1));
            }
        }
        catch(SQLException e)
        {
             System.out.println(e);
        }
    }
    
