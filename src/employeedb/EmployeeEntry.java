    public void checkData()
    {
        if(txtName.getText().trim().equals(""))
        {
            JOptionPane.showMessageDialog(this,"You must enter Employee's Name!");
            txtName.requestFocus();
        }
        else if(txtNRC.getText().trim().equals(""))
        {
            JOptionPane.showMessageDialog(this,"You must enter NRC Number!");
            txtNRC.requestFocus();
        }
        else if(txtAddress.getText().trim().equals(""))
        {
            JOptionPane.showMessageDialog(this,"You must enter Address!");
            txtAddress.requestFocus();
        }
        else if(txtQualification.getText().trim().equals(""))
        {
            JOptionPane.showMessageDialog(this,"You must enter Qualification!");
            txtQualification.requestFocus();
        }
        else if(gender==null || gender.equals(""))
        {
            JOptionPane.showMessageDialog(this,"You must choose Gender!");
        }
        else if(cboDepartment.getSelectedIndex()==0)
        {
            JOptionPane.showMessageDialog(this,"You must choose Department!");
            cboDepartment.requestFocus();
        }
        else if(cboPost.getSelectedIndex()==0)
        {
            JOptionPane.showMessageDialog(this,"You must choose Post!");
            cboPost.requestFocus();
        }
        else if(lblEmployeeID.getText().equals(""))
        {
            AutoID();
        }
        else
        {
            saveRecord();
        }
    }
    
    public void saveRecord()
    {
        try
        {
            ste=con.createStatement();
            String sql="Select * From Employee Where EmployeeName='"+txtName.getText()+"' and NRC='"+txtNRC.getText()+"'";
            rs=ste.executeQuery(sql);
            if(rs.next())
            {
                JOptionPane.showMessageDialog(this,"This record is already exit.");
                txtName.requestFocus();
                txtName.selectAll();
                return;
            }
            sql="Insert into Employee Values("+lblEmployeeID.getText().trim()+",'"+txtName.getText().trim()+"','"+txtNRC.getText().trim()+"','"+gender+"','"+txtAddress.getText().trim()+"','"+cboDepartment.getSelectedItem().toString().trim()+"','"+cboPost.getSelectedItem().toString().trim()+"','"+txtQualification.getText().trim()+"')";
            int result=ste.executeUpdate(sql);
            if(result==1)
            {
                AutoID();
                clearData();
                JOptionPane.showMessageDialog(this,"This Employee record is successfully saved!");
            }
        }
        catch(SQLException sqle)
        {
            System.out.println(sqle);
            JOptionPane.showMessageDialog(this,"Saved record is failure.");
        }
        catch(Exception e)
        {
            System.out.println(e);
        }
    }
